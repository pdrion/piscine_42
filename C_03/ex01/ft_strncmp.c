int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int i;

	i = 0;
	while (i < (n - 1) && (*s1 && *s2) && *s1 == *s2)
	{
		s1++;
		s2++;
		i++;
	}
	if (*s1 == *s2 || n == 0)
		return (0);
	else
		return (*s1 - *s2);
}
