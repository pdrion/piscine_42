void	ft_rev_int_tab(int *tab, int size)
{
	int		tmp[size];
	int		i;

	i = 0;
	while (i < size)
	{
		tmp[i] = *(tab + size - 1 - i);
		i++;
	}
	i = 0;
	while (i < size)
	{
		*(tab + i) = tmp[i];
		i++;
	}
}
