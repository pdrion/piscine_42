void	ft_sort_int_tab(int *tab, int size)
{
	int		i;
	int		a;

	i = 0;
	while (i < size - 1)
	{
		while (tab[i] > tab[i + 1])
		{
			a = tab[i];
			tab[i] = tab[i + 1];
			tab[i + 1] = a;
			i = 0;
		}
		i++;
	}
}
